function U(prefix: string, titles: string[]): ApUnit[] {
 return titles.map((title, i) => ({
 id: `${prefix}-u${i + 1}`,
 index: i + 1,
 title,
 summary: `Practice for Unit ${i + 1}: ${title}.`,
 }));
}

